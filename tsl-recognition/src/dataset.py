"""
Dataset scanning, normalization, and DataLoader creation.

Provides the memory-efficient ``LazySignDataset`` that loads ``.npy`` keypoint
files on demand, plus helpers to compute / load a ``StandardScaler`` and build
train/val/test ``DataLoader`` instances with optional class-weighted sampling.

Data splits are loaded from persistent JSON manifests generated by
``src.split``.  If no manifests exist the user is prompted to run the
``split`` subcommand first.
"""

from __future__ import annotations

import json
import pickle
from collections import Counter
from pathlib import Path

import numpy as np
import torch
from sklearn.preprocessing import StandardScaler
from torch.utils.data import DataLoader, Dataset, WeightedRandomSampler
from tqdm.auto import tqdm

from .augmentation import (
    AugmentConfig,
    apply_landmark_dropout,
    augment_spatial,
    augment_temporal,
)
from .config import DEVICE, SEED, TrainConfig


# ---------------------------------------------------------------------------
# DataLoader worker seeding (reproducibility)
# ---------------------------------------------------------------------------
def _worker_init_fn(worker_id: int) -> None:
    """Seed NumPy in each DataLoader worker from PyTorch's per-worker seed.

    Without this, ``np.random.default_rng()`` (used for augmentation) draws
    entropy from the OS, making augmentation non-reproducible across runs.
    PyTorch assigns each worker a unique seed derived from the base generator;
    we forward that seed to NumPy so the augmentation RNG sequence is
    deterministic.  With ``persistent_workers=True`` the NumPy state advances
    naturally across epochs, so augmentations still vary epoch-to-epoch.
    """
    worker_info = torch.utils.data.get_worker_info()
    if worker_info is not None:
        np.random.seed(worker_info.seed % (2**32))


# ---------------------------------------------------------------------------
# Lazy-loading dataset
# ---------------------------------------------------------------------------
class LazySignDataset(Dataset):
    """Memory-efficient dataset that loads samples on-demand from disk."""

    def __init__(
        self,
        file_info_list: list[tuple[str, int, int]],
        max_seq_len: int,
        feature_dim: int,
        scaler: StandardScaler | None = None,
        augment: bool = False,
        augment_cfg: AugmentConfig | None = None,
        sequence_handling: str = "uniform_sample",
    ):
        """
        Parameters
        ----------
        file_info_list : list of (path, label, num_frames)
        max_seq_len : maximum sequence length (pad / truncate)
        feature_dim : expected feature vector width
        scaler : optional StandardScaler for normalisation
        augment : if True, apply data augmentation (training only)
        augment_cfg : augmentation hyper-parameters (uses defaults if None)
        sequence_handling : "truncate" (keep first N frames) or
            "uniform_sample" (evenly sample N frames from full sequence)
        """
        self.file_info = file_info_list
        self.max_seq_len = max_seq_len
        self.feature_dim = feature_dim
        self.scaler = scaler
        self.augment = augment
        self.augment_cfg = augment_cfg or AugmentConfig()
        self.sequence_handling = sequence_handling

    def __len__(self) -> int:
        return len(self.file_info)

    def __getitem__(self, idx: int):
        """Load and preprocess a single sample (keypoint sequence).

        Processing order
        ----------------
        1. Load raw keypoints from disk.
        2. **Temporal augmentation** on raw data (may change frame count).
        3. Reduce to ``max_seq_len`` via uniform temporal sampling (default)
           or simple truncation (legacy).
        4. Normalise with the pre-computed scaler.
        5. **Spatial augmentation** in normalised space (jitter, scale,
           translate).  Operating in normalised space is critical — 28 %
           of features have raw scale < 0.01, so raw-space perturbations
           get amplified by 10–180 000× after normalisation.
        6. **Landmark dropout** in normalised space (zeroed features sit
           at the normalised mean 0, not at extreme −mean/scale values).
        7. Pad to ``max_seq_len``.

        Parameters
        ----------
        idx : int
            Index of the sample to retrieve.

        Returns
        -------
        tuple[torch.Tensor, torch.Tensor, torch.Tensor]
            - keypoints tensor of shape (max_seq_len, feature_dim)
            - label tensor (scalar) indicating the sign class
            - actual sequence length tensor (scalar) — number of real
              (non-padded) frames; models use this to build padding masks
        """
        # Extract file path and label from metadata
        path, label, _ = self.file_info[idx]

        # 1. Load keypoint sequence from disk
        keypoints = np.load(path).astype(np.float32)

        # A single RNG per call — seeded from the (deterministic) global
        # NumPy state so augmentation is reproducible across runs yet still
        # varies across epochs and samples (the global state advances with
        # each call).  Requires _worker_init_fn on multi-worker DataLoaders.
        rng = (
            np.random.default_rng(np.random.randint(0, 2**31))
            if self.augment
            else None
        )

        # 2. Temporal augmentation (raw space, before truncation)
        if self.augment:
            keypoints = augment_temporal(keypoints, rng=rng, cfg=self.augment_cfg)

        num_frames = keypoints.shape[0]

        # 3. Uniform temporal sampling (or truncation)
        if num_frames > self.max_seq_len:
            if self.sequence_handling == "uniform_sample":
                # Evenly sample max_seq_len frames across the full sequence
                # so no temporal region is discarded
                indices = np.linspace(
                    0, num_frames - 1, self.max_seq_len
                ).round().astype(int)
                keypoints = keypoints[indices]
            else:
                # Legacy truncation: keep only the first max_seq_len frames
                keypoints = keypoints[: self.max_seq_len]
            num_frames = self.max_seq_len

        # Record actual (non-padded) length before padding
        actual_length = num_frames

        # 4. Normalise (zero mean, unit variance)
        if self.scaler is not None:
            keypoints = (keypoints - self.scaler.mean_) / self.scaler.scale_

        # 5–6. Spatial augmentation + landmark dropout (normalised space)
        if self.augment:
            keypoints = augment_spatial(keypoints, rng=rng, cfg=self.augment_cfg)
            keypoints = apply_landmark_dropout(
                keypoints, rng=rng, cfg=self.augment_cfg
            )

        # 7. Pad
        if num_frames < self.max_seq_len:
            padding = np.zeros(
                (self.max_seq_len - num_frames, self.feature_dim), dtype=np.float32
            )
            keypoints = np.vstack([keypoints, padding])

        return (
            torch.tensor(keypoints, dtype=torch.float32),
            torch.tensor(label, dtype=torch.long),
            torch.tensor(actual_length, dtype=torch.long),
        )


# ---------------------------------------------------------------------------
# Scan dataset
# ---------------------------------------------------------------------------
def scan_dataset(
    cfg: TrainConfig,
    data_path: Path | None = None,
) -> tuple[list[tuple[str, int, int]], int]:
    """Scan the processed keypoint directory and collect metadata.

    Returns
    -------
    file_paths : list of (npy_path, label_int, actual_frames)
    feature_dim : detected feature dimension
    """
    data_path = data_path or cfg.dataset_info.processed_dir
    actions = cfg.actions
    label_map = {label: num for num, label in enumerate(actions)}

    print(f"Scanning dataset at {data_path}...")
    print(f"Max sequence length: {cfg.max_sequence_length}, Min: {cfg.min_sequence_length}")

    file_paths: list[tuple[str, int, int]] = []
    expected_dim: int | None = None
    skipped = 0

    # Iterate through all sign classes and collect metadata about their .npy files
    for action in tqdm(actions, desc="Scanning classes"):
        action_dir = data_path / action
        if not action_dir.exists():
            # Skip if directory doesn't exist for this sign class
            continue
        for npy_path in sorted(action_dir.glob("*.npy")):
            try:
                # Load keypoint array using memory mapping (efficient for large files)
                # mmap_mode="r" means read-only, doesn't load entire file into RAM
                keypoints = np.load(npy_path, mmap_mode="r")
                # Validate that keypoints have correct shape (2D array)
                if len(keypoints.shape) != 2:
                    skipped += 1
                    continue
                num_frames, num_features = keypoints.shape
                # Skip sequences that are too short
                if num_frames < cfg.min_sequence_length:
                    skipped += 1
                    continue
                # Check feature dimension consistency across all files
                if expected_dim is None:
                    expected_dim = num_features  # Set from first file
                elif num_features != expected_dim:
                    # Skip files with mismatched dimensions
                    skipped += 1
                    continue
                # Calculate actual frames (capped at max_sequence_length)
                actual_frames = min(num_frames, cfg.max_sequence_length)
                # Store metadata: (file_path, label_index, actual_frame_count)
                file_paths.append((str(npy_path), label_map[action], actual_frames))
            except Exception as e:
                print(f"Error scanning {npy_path}: {e}")
                skipped += 1

    if not file_paths:
        raise RuntimeError("No valid sequences found. Run keypoint extraction first.")

    assert expected_dim is not None
    seq_lengths = [f[2] for f in file_paths]
    print(f"\nFound: {len(file_paths)} valid sequences, Skipped: {skipped}")
    print(f"Feature dimension: {expected_dim}")
    print(
        f"Sequence lengths - min: {min(seq_lengths)}, max: {max(seq_lengths)}, "
        f"mean: {np.mean(seq_lengths):.1f}"
    )
    return file_paths, expected_dim


# ---------------------------------------------------------------------------
# Scaler (normalisation statistics)
# ---------------------------------------------------------------------------
def get_or_compute_scaler(
    train_files: list[tuple[str, int, int]],
    expected_dim: int,
    cfg: TrainConfig,
    data_path: Path | None = None,
) -> StandardScaler | None:
    """Load or incrementally compute a ``StandardScaler`` for feature normalisation.

    Important: the scaler is computed from **training files only** to prevent
    data leakage from the validation / test sets.  A separate scaler file is
    saved per split mode (``scaler_signer.pkl`` / ``scaler_random.pkl``) so
    experiments with different splits don't overwrite each other.
    """
    if not cfg.normalize_features:
        print("Feature normalization: OFF")
        return None

    data_path = data_path or cfg.dataset_info.processed_dir
    scaler_path = data_path / f"scaler_{cfg.split_mode}.pkl"

    if scaler_path.exists():
        print(f"\nLoading pre-computed scaler from {scaler_path}...")
        with open(scaler_path, "rb") as f:
            scaler: StandardScaler = pickle.load(f)
        print(f"Scaler loaded: mean shape={scaler.mean_.shape}")
        return scaler

    print(f"\nComputing normalization statistics from {len(train_files)} training files...")
    # Use Welford's online algorithm for computing mean and variance incrementally
    # This is memory-efficient as it doesn't require loading all data at once
    n_total = 0  # Total number of frames processed
    mean = np.zeros(expected_dim, dtype=np.float64)  # Running mean
    M2 = np.zeros(expected_dim, dtype=np.float64)  # Running sum of squared differences

    # Iterate through TRAINING keypoint files only
    for path, _, _ in tqdm(train_files, desc="Computing stats (train only)"):
        keypoints = np.load(path, mmap_mode="r")  # Memory-mapped loading
        num_frames = min(len(keypoints), cfg.max_sequence_length)
        # Process each frame in the sequence
        for i in range(num_frames):
            n_total += 1
            # Welford's algorithm update steps:
            delta = keypoints[i].astype(np.float64) - mean  # Difference from old mean
            mean += delta / n_total  # Update mean
            delta2 = keypoints[i].astype(np.float64) - mean  # Difference from new mean
            M2 += delta * delta2  # Update sum of squared differences

    # Calculate variance and standard deviation from accumulated statistics
    variance = M2 / n_total
    std = np.sqrt(variance)
    std[std < 1e-8] = 1.0  # Prevent division by zero for features with no variance

    scaler = StandardScaler()
    scaler.mean_ = mean.astype(np.float32)
    scaler.scale_ = std.astype(np.float32)
    scaler.var_ = variance.astype(np.float32)
    scaler.n_features_in_ = expected_dim
    scaler.n_samples_seen_ = n_total

    with open(scaler_path, "wb") as f:
        pickle.dump(scaler, f)
    print(f"Scaler computed and saved to {scaler_path}")
    print(f"  Training frames processed: {n_total:,}")
    return scaler


# ---------------------------------------------------------------------------
# DataLoader builder
# ---------------------------------------------------------------------------
def build_loaders(
    cfg: TrainConfig,
    data_path: Path | None = None,
) -> dict:
    """Load persisted splits, normalise, and return everything needed for training.

    Requires split manifests to exist (generated by ``src.split.generate_split``).

    Returns a dict with keys:
        train_loader, val_loader, test_loader,
        train_ds, val_ds, test_ds,
        scaler, class_weights, feature_dim, label_map,
        y_train, y_val, y_test,
        train_files, val_files, test_files, split_mode
    """
    # ---- Load persisted split manifests (keep class_name for remapping) ----
    split_dir = cfg.dataset_info.split_dir
    meta_path = split_dir / "split_meta.json"
    if not meta_path.exists():
        raise FileNotFoundError(
            f"No split manifests found in {split_dir}. "
            "Run `python -m src.main split` first."
        )

    with open(meta_path) as f:
        meta = json.load(f)
    feature_dim = int(meta["feature_dim"])
    split_mode = str(meta["mode"])

    actions = cfg.actions
    allowed_classes = set(actions.tolist())
    label_map = {label: num for num, label in enumerate(actions)}

    def _load_partition(partition: str) -> list[tuple[str, int, int]]:
        manifest_path = split_dir / f"{partition}.json"
        with open(manifest_path) as f:
            entries = json.load(f)
        kept: list[tuple[str, int, int]] = []
        dropped = 0
        for e in entries:
            cls = e.get("class_name")
            if cls not in allowed_classes:
                dropped += 1
                continue
            kept.append((e["path"], label_map[cls], int(e["num_frames"])))
        if dropped:
            print(f"Filtered {partition} split: dropped {dropped} samples outside cfg.classes_to_process")
        return kept

    train_files = _load_partition("train")
    val_files = _load_partition("val")
    test_files = _load_partition("test")

    print(f"\nLoaded {split_mode} split: "
          f"{len(train_files)} train / {len(val_files)} val / {len(test_files)} test")

    # ---- Scaler (computed from training data only) ----
    scaler = get_or_compute_scaler(train_files, feature_dim, cfg, data_path)

    # ---- Class weights (computed from training partition only) ----
    train_labels = [f[1] for f in train_files]
    class_counts = Counter(train_labels)
    print(f"\nTraining class distribution:")
    print(f"  Min samples: {min(class_counts.values())}, Max: {max(class_counts.values())}")
    print(
        f"  Imbalance ratio: "
        f"{max(class_counts.values()) / max(min(class_counts.values()), 1):.1f}x"
    )

    total_train = len(train_files)
    class_weights = torch.tensor(
        [
            total_train / (len(class_counts) * class_counts.get(i, 1))
            for i in range(cfg.num_classes)
        ],
        dtype=torch.float32,
    ).to(DEVICE)
    class_weights = class_weights / class_weights.mean()
    print(f"  Class weights range: [{class_weights.min():.2f}, {class_weights.max():.2f}]")

    # ---- Labels ----
    y_train = np.array([f[1] for f in train_files])
    y_val = np.array([f[1] for f in val_files])
    y_test = np.array([f[1] for f in test_files])

    # ---- Datasets ----
    seq_handling = cfg.sequence_handling
    train_ds = LazySignDataset(
        train_files, cfg.max_sequence_length, feature_dim, scaler,
        augment=cfg.augment_train,
        sequence_handling=seq_handling,
    )
    val_ds = LazySignDataset(
        val_files, cfg.max_sequence_length, feature_dim, scaler,
        sequence_handling=seq_handling,
    )
    test_ds = LazySignDataset(
        test_files, cfg.max_sequence_length, feature_dim, scaler,
        sequence_handling=seq_handling,
    )

    # ---- DataLoaders ----
    nw = cfg.num_workers
    if cfg.use_weighted_sampling:
        sample_weights = [class_weights[label].item() for _, label, _ in train_files]
        sampler = WeightedRandomSampler(
            sample_weights,
            num_samples=len(sample_weights),
            replacement=True,
        )
        train_loader = DataLoader(
            train_ds,
            batch_size=cfg.batch_size,
            sampler=sampler,
            num_workers=nw,
            pin_memory=True,
            persistent_workers=nw > 0,
            worker_init_fn=_worker_init_fn,
        )
        print("Using WeightedRandomSampler for balanced batches")
    else:
        train_loader = DataLoader(
            train_ds,
            batch_size=cfg.batch_size,
            shuffle=True,
            num_workers=nw,
            pin_memory=True,
            persistent_workers=nw > 0,
            worker_init_fn=_worker_init_fn,
        )

    val_loader = DataLoader(
        val_ds,
        batch_size=cfg.batch_size,
        shuffle=False,
        num_workers=nw,
        pin_memory=True,
        persistent_workers=nw > 0,
        worker_init_fn=_worker_init_fn,
    )

    test_loader = DataLoader(
        test_ds,
        batch_size=cfg.batch_size,
        shuffle=False,
        num_workers=nw,
        pin_memory=True,
        persistent_workers=nw > 0,
        worker_init_fn=_worker_init_fn,
    )

    if cfg.augment_train:
        print("Training data augmentation: ON (temporal + spatial + landmark dropout)")
    else:
        print("Training data augmentation: OFF")
    print(f"Sequence handling: {seq_handling} (padding masks returned to models)")

    print(f"\nDataset (lazy-loaded, {split_mode} split):")
    print(f"  X_train shape: ({len(train_files)}, {cfg.max_sequence_length}, {feature_dim})")
    print(f"  X_val   shape: ({len(val_files)}, {cfg.max_sequence_length}, {feature_dim})")
    print(f"  X_test  shape: ({len(test_files)}, {cfg.max_sequence_length}, {feature_dim})")
    print(f"Train batches: {len(train_loader)}, "
          f"Val batches: {len(val_loader)}, "
          f"Test batches: {len(test_loader)}")
    print(f"Workers: {nw}, Pin memory: True")
    print(f"\nMemory efficient: Data loaded on-demand from disk!")

    return {
        "train_loader": train_loader,
        "val_loader": val_loader,
        "test_loader": test_loader,
        "train_ds": train_ds,
        "val_ds": val_ds,
        "test_ds": test_ds,
        "scaler": scaler,
        "class_weights": class_weights,
        "feature_dim": feature_dim,
        "label_map": label_map,
        "y_train": y_train,
        "y_val": y_val,
        "y_test": y_test,
        "train_files": train_files,
        "val_files": val_files,
        "test_files": test_files,
        "split_mode": split_mode,
    }
